import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const schemaPath = resolve(root, "protocol/keel.schema.json");

const outFiles = {
  ts: resolve(root, "packages/protocol/src/generated.ts"),
  rs: resolve(root, "crates/keel-protocol/src/generated.rs")
};

function tsType(type) {
  if (type.endsWith("[]")) return `${tsType(type.slice(0, -2))}[]`;
  if (type === "string") return "string";
  if (type === "number") return "number";
  if (type === "boolean") return "boolean";
  if (type === "json") return "unknown";
  return type;
}

function rustType(type) {
  if (type.endsWith("[]")) return `Vec<${rustType(type.slice(0, -2))}>`;
  if (type === "string") return "String";
  if (type === "number") return "f64";
  if (type === "boolean") return "bool";
  if (type === "json") return "serde_json::Value";
  return type;
}

function rustFieldName(name) {
  return name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toLowerCase();
}

function methodConstName(name) {
  return name.toUpperCase().replaceAll(".", "_").replaceAll("-", "_");
}

function methodVariantName(name) {
  return name
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => `${part[0].toUpperCase()}${part.slice(1)}`)
    .join("");
}

function renderTs(schema) {
  const lines = [
    "// Generated by tools/generate-protocol.mjs. Do not edit by hand.",
    "",
    `export const protocolVersion = ${JSON.stringify(schema.version)} as const;`,
    ""
  ];

  for (const record of schema.records) {
    lines.push(`export interface ${record.name} {`);
    for (const field of record.fields) {
      lines.push(`  ${field.name}${field.optional ? "?" : ""}: ${tsType(field.type)};`);
    }
    lines.push("}", "");
  }

  lines.push("export interface MethodMap {");
  for (const method of schema.methods) {
    lines.push(`  ${JSON.stringify(method.name)}: { params: ${method.params}; result: ${method.result} };`);
  }
  lines.push("}", "");

  lines.push("export type MethodName = keyof MethodMap;");
  lines.push("export type RpcRequest<M extends MethodName = MethodName> = {");
  lines.push("  id: string;");
  lines.push("  method: M;");
  lines.push("  params: MethodMap[M][\"params\"];");
  lines.push("};");
  lines.push("export type RpcResponse<M extends MethodName = MethodName> = {");
  lines.push("  id: string;");
  lines.push("  result?: MethodMap[M][\"result\"];");
  lines.push("  error?: RpcError;");
  lines.push("};");
  lines.push("");

  lines.push("export const methods = {");
  for (const method of schema.methods) {
    lines.push(`  ${methodConstName(method.name)}: ${JSON.stringify(method.name)},`);
  }
  lines.push("} as const;");
  lines.push("");

  return `${lines.join("\n")}\n`;
}

function renderRust(schema) {
  const lines = [
    "// Generated by tools/generate-protocol.mjs. Do not edit by hand.",
    "",
    "use serde::{Deserialize, Serialize};",
    "",
    `pub const PROTOCOL_VERSION: &str = ${JSON.stringify(schema.version)};`,
    ""
  ];

  for (const record of schema.records) {
    lines.push("#[derive(Clone, Debug, Deserialize, Serialize)]");
    lines.push(`pub struct ${record.name} {`);
    for (const field of record.fields) {
      const rustName = rustFieldName(field.name);
      if (rustName !== field.name) {
        lines.push(`    #[serde(rename = ${JSON.stringify(field.name)})]`);
      }
      if (field.optional) {
        lines.push("    #[serde(skip_serializing_if = \"Option::is_none\")]");
      }
      const type = field.optional ? `Option<${rustType(field.type)}>` : rustType(field.type);
      lines.push(`    pub ${rustName}: ${type},`);
    }
    lines.push("}", "");
  }

  lines.push("#[derive(Clone, Debug, Deserialize, Serialize)]");
  lines.push("#[serde(tag = \"method\", content = \"params\")]");
  lines.push("pub enum RpcRequestParams {");
  for (const method of schema.methods) {
    lines.push(`    #[serde(rename = ${JSON.stringify(method.name)})]`);
    lines.push(`    ${methodVariantName(method.name)}(${method.params}),`);
  }
  lines.push("}", "");

  for (const method of schema.methods) {
    lines.push(`pub const ${methodConstName(method.name)}: &str = ${JSON.stringify(method.name)};`);
  }
  lines.push("");

  return `${lines.join("\n")}\n`;
}

export async function generate() {
  const schema = JSON.parse(await readFile(schemaPath, "utf8"));
  return {
    ts: renderTs(schema),
    rs: renderRust(schema)
  };
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const generated = await generate();
  await writeFile(outFiles.ts, generated.ts);
  await writeFile(outFiles.rs, generated.rs);
  console.log("Generated protocol bindings.");
}
